// ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡πÅ‡∏™‡∏î‡∏á‡∏¢‡∏≠‡∏î‡πÄ‡∏á‡∏¥‡∏ô
function showBalance(bot, chatId, userId) {
  const data = loadData();
  if (!data[userId]) {
    data[userId] = { balance: 0 };
    saveData(data);
  }
  const balance = data[userId].balance;
  bot.sendMessage(chatId, `üí∞ ‡∏¢‡∏≠‡∏î‡πÄ‡∏á‡∏¥‡∏ô‡∏õ‡∏±‡∏à‡∏à‡∏∏‡∏ö‡∏±‡∏ô‡∏Ç‡∏≠‡∏á‡∏Ñ‡∏∏‡∏ì: ${formatMoney(balance)}`);
}

// ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡πÄ‡∏™‡∏Å‡πÄ‡∏á‡∏¥‡∏ô (‡πÄ‡∏â‡∏û‡∏≤‡∏∞‡πÅ‡∏≠‡∏î‡∏°‡∏¥‡∏ô)
function generateMoney(bot, chatId, userId, amount) {
  const data = loadData();
  if (!data[userId]) {
    data[userId] = { balance: 0 };
  }

  // ‡πÄ‡∏™‡∏Å‡πÄ‡∏á‡∏¥‡∏ô‡∏ï‡∏≤‡∏°‡∏à‡∏≥‡∏ô‡∏ß‡∏ô‡∏ó‡∏µ‡πà‡∏£‡∏∞‡∏ö‡∏∏
  data[userId].balance += amount;
  saveData(data);

  bot.sendMessage(chatId, `‚ú® ‡∏Ñ‡∏∏‡∏ì‡πÑ‡∏î‡πâ‡πÄ‡∏™‡∏Å‡πÄ‡∏á‡∏¥‡∏ô ${formatMoney(amount)} ‡πÄ‡∏£‡∏µ‡∏¢‡∏ö‡∏£‡πâ‡∏≠‡∏¢!\nüí∞ ‡∏¢‡∏≠‡∏î‡πÄ‡∏á‡∏¥‡∏ô‡∏õ‡∏±‡∏à‡∏à‡∏∏‡∏ö‡∏±‡∏ô: ${formatMoney(data[userId].balance)}`);
}

// ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏à‡∏±‡∏î‡∏£‡∏π‡∏õ‡πÅ‡∏ö‡∏ö‡πÄ‡∏á‡∏¥‡∏ô (‡∏†‡∏≤‡∏©‡∏≤‡πÑ‡∏ó‡∏¢)
function formatMoney(amount) {
  const baht = Math.floor(amount);
  const satang = Math.round((amount - baht) * 100);
  return `${baht} ‡∏ö‡∏≤‡∏ó ${satang} ‡∏™‡∏ï‡∏≤‡∏á‡∏Ñ‡πå`;
}
